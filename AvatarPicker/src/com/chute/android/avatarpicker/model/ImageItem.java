package com.chute.android.avatarpicker.model;

/**
 * The {@link ImageItem} class represents the concept of an image. Each image
 * contains contains file path and which service it belongs to.
 * 
 */

public class ImageItem {

	public static final String TAG = ImageItem.class.getSimpleName();

	/**
	 * The unique identifier of the image.
	 */
	private long id;
	/**
	 * The file path of the image.
	 */
	private String filePath;
	/**
	 * The service the image belongs to. Available services: Facebook, Flickr,
	 * Picasa, Instagram, Twitter and Gravatar.
	 */
	private String service;

	/**
	 * Getters and setters
	 * 
	 * @return
	 */
	public long getId() {
		return id;
	}

	public void setId(long id) {
		this.id = id;
	}

	public String getFilePath() {
		return filePath;
	}

	public void setFilePath(String filePath) {
		this.filePath = filePath;
	}

	public String getService() {
		return service;
	}

	public void setService(String service) {
		this.service = service;
	}

	/*
	 * (non-Javadoc)
	 * 
	 * @see java.lang.Object#toString()
	 */
	@Override
	public String toString() {
		return "ImageItem [id=" + id + ", filePath=" + filePath + ", service="
				+ service + "]";
	}

	/*
	 * (non-Javadoc)
	 * 
	 * @see java.lang.Object#hashCode()
	 */
	@Override
	public int hashCode() {
		final int prime = 31;
		int result = 1;
		result = prime * result
				+ ((filePath == null) ? 0 : filePath.hashCode());
		result = prime * result + (int) (id ^ (id >>> 32));
		result = prime * result + ((service == null) ? 0 : service.hashCode());
		return result;
	}

	/*
	 * (non-Javadoc)
	 * 
	 * @see java.lang.Object#equals(java.lang.Object)
	 */
	@Override
	public boolean equals(Object obj) {
		if (this == obj)
			return true;
		if (obj == null)
			return false;
		if (getClass() != obj.getClass())
			return false;
		ImageItem other = (ImageItem) obj;
		if (filePath == null) {
			if (other.filePath != null)
				return false;
		} else if (!filePath.equals(other.filePath))
			return false;
		if (id != other.id)
			return false;
		if (service == null) {
			if (other.service != null)
				return false;
		} else if (!service.equals(other.service))
			return false;
		return true;
	}

}
